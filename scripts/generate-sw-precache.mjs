// Generates public/sw-precache.js (the service worker's precache URL list +
// content-hash version) and stamps the version into public/sw.js so the main
// SW script changes every deploy. Committed output; verify with --check.
import { createHash } from 'node:crypto';
import { readdirSync, readFileSync, statSync, writeFileSync, existsSync } from 'node:fs';
import { extname, join, relative, sep } from 'node:path';
import { fileURLToPath } from 'node:url';

const PUBLIC_DIR = fileURLToPath(new URL('../public/', import.meta.url));
const OUTPUT = join(PUBLIC_DIR, 'sw-precache.js');
const SW_FILE = join(PUBLIC_DIR, 'sw.js');
const STAMP = /^\/\/ precache-version: \w+$/m;
const TEXT_EXTENSIONS = new Set(['.css', '.html', '.js', '.webmanifest']);

// Never precache: the SW itself + its imports (browser-managed), CI-rewritten
// config (seeded at install + network-first at runtime; excluded from the
// hash so local and CI contents may differ), and non-app pages.
const EXCLUDE = new Set([
    'sw.js',
    'sw-precache.js',
    'js/config.js',
    'js/config.example.js',
    'layout-mockups.html'
]);

function walk(dir) {
    return readdirSync(dir).flatMap((name) => {
        // .DS_Store & co: gitignored, vary per machine, and a committed
        // precache entry that 404s makes SW install fail outright.
        if (name.startsWith('.')) return [];
        const full = join(dir, name);
        return statSync(full).isDirectory() ? walk(full) : [full];
    });
}

const files = walk(PUBLIC_DIR)
    .map((f) => relative(PUBLIC_DIR, f).split(sep).join('/'))
    .filter((f) => !EXCLUDE.has(f))
    .sort();

const hash = createHash('sha256');
for (const f of files) {
    const contents = readFileSync(join(PUBLIC_DIR, f));
    const canonicalContents = TEXT_EXTENSIONS.has(extname(f))
        ? contents.toString('utf8').replace(/\r\n/g, '\n')
        : contents;
    hash.update(f).update(canonicalContents);
}
const version = hash.digest('hex').slice(0, 12);

const urls = ['/', ...files.map((f) => `/${f}`)];
const banner = '// GENERATED by scripts/generate-sw-precache.mjs — do not edit.\n';
const body = `${banner}const PRECACHE_VERSION = '${version}';\nconst PRECACHE_URLS = ${JSON.stringify(urls, null, 4)};\n`;

// sw.js is EXCLUDEd from the hash, so stamping it is not circular. Tolerate
// its absence (this task runs before Task 5 creates it).
const swCurrent = existsSync(SW_FILE) ? readFileSync(SW_FILE, 'utf8') : null;
if (swCurrent !== null && !STAMP.test(swCurrent)) {
    console.error('public/sw.js is missing its "// precache-version:" stamp line.');
    process.exit(1);
}
const swStamped =
    swCurrent === null ? null : swCurrent.replace(STAMP, `// precache-version: ${version}`);

if (process.argv.includes('--check')) {
    const current = existsSync(OUTPUT) ? readFileSync(OUTPUT, 'utf8') : '';
    if (current !== body || (swStamped !== null && swCurrent !== swStamped)) {
        console.error('sw precache artifacts are stale. Run: npm run build:sw-precache');
        process.exit(1);
    }
    console.log('sw-precache up to date.');
} else {
    writeFileSync(OUTPUT, body);
    if (swStamped !== null) writeFileSync(SW_FILE, swStamped);
    console.log(`wrote public/sw-precache.js (${urls.length} urls, version ${version})`);
}
